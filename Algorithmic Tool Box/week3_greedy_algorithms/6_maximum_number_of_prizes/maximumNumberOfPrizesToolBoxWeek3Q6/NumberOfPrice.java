package maximumNumberOfPrizesToolBoxWeek3Q6;

import java.util.List;

public class NumberOfPrice {
	
	long numberOfPrice;
	
	List<Long> priceList;

	public NumberOfPrice(long numberOfPrice, List<Long> priceList) {
		this.numberOfPrice = numberOfPrice;
		this.priceList = priceList;
	}

	public long getNumberOfPrice() {
		return numberOfPrice;
	}

	public List<Long> getPriceList() {
		return priceList;
	}
	
	

}
