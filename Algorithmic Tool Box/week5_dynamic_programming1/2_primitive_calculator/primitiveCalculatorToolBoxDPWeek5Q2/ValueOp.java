package primitiveCalculatorToolBoxDPWeek5Q2;

import java.util.List;

public class ValueOp {
	
	int minOp;
	
	List<Integer> valueOp;

	public ValueOp(int minOp, List<Integer> valueOp) {
		this.minOp = minOp;
		this.valueOp = valueOp;
	}

	public int getMinOp() {
		return minOp;
	}

	public List<Integer> getValueOp() {
		return valueOp;
	}

}
